#include "../includes/libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*tmp;
	int		size;

	tmp = lst;
	size = 0;
	if (!tmp)
		return (0);
	while (tmp && tmp->next)
	{
		size++;
		tmp = tmp->next;
	}
	return (++size);
}
